package com.example.kotlinbasics

fun main () {

    // Step 1: Declaring Boolean Variables
    var myTrue = true
    var myFalse = false

    //Step 2: Logical OR Operation

    println(myTrue || myFalse) //output : true

    //Step 3: Logical AND Operation
    println(myTrue && myFalse) //output : false

    //Step 4: Logical NOT Operation
    println(!myTrue) //output : false
}